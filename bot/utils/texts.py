def get_loading_step(step: int) -> Dict[str, Any]:
    """
    Получить шаг анимации загрузки
    
    Args:
        step: Номер шага (0-4)
    
    Returns:
        Dict с текстом и задержкой
    """
    if 0 <= step < len(LOADING_ANIMATION_STEPS):
        return LOADING_ANIMATION_STEPS[step]
    return {"text": "", "delay": 0}


