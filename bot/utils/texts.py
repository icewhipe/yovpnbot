def format_balance(balance: float) -> str:
    """Форматирует баланс"""
    return f"{balance:.2f}₽"


def format_days(days: int) -> str:
    """Форматирует дни с правильным склонением"""
    if days % 10 == 1 and days % 100 != 11:
        return f"{days} день"
    elif days % 10 in [2, 3, 4] and days % 100 not in [12, 13, 14]:
        return f"{days} дня"
    else:
        return f"{days} дней"
